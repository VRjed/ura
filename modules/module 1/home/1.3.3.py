n = int(input())
p = 3.141592
l = 2 * p * n
s = p * (n * n)
v = 4/3 * p * (n*n*n)
w = 4 * p * (n*n)
print("Длина окружности радиуса" ,n,"равна", round(l,3))
print("Площадь круга радиуса" ,n,"равна", round(s,3))
print("Объем шара радиуса" ,n,"равен", round(v,3))
print("Площадь поверхности шара радиуса" ,n,"равна", round(w,3))